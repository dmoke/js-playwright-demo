// Variables with different types
let numberVar = 42;
let stringVar = "42";

// Using == for loose equality (value equality)
console.log(numberVar == stringVar); // true

// Using === for strict equality (value and type equality)
console.log(numberVar === stringVar); // false

// Additional example
let booleanVar = true;

// Using == for loose equality
console.log(numberVar == booleanVar); // false

// Using === for strict equality
console.log(numberVar === booleanVar); // false
