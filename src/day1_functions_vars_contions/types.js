// String
let stringValue = "Hello, World!";
console.log("String:", typeof stringValue);

// Number
let numberValue = 42;
console.log("Number:", typeof numberValue);

// Boolean
let booleanValue = true;
console.log("Boolean:", typeof booleanValue);

// Object
let objectValue = { key: "value" };
console.log("Object:", typeof objectValue);

// Array
let arrayValue = [1, 2, 3];
console.log("Array:", typeof arrayValue);

// Function
let functionValue = function () {
  console.log("Function");
};
console.log("Function:", typeof functionValue);

// Undefined
let undefinedValue;
console.log("Undefined:", typeof undefinedValue);

// Null
let nullValue = null;
console.log("Null:", typeof nullValue);
